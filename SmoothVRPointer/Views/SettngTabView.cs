        #region // パブリックメソッド
        public void Initialize()
        {
            this.PropertyChanged += this.SettngTabView_PropertyChanged;
            GameplaySetup.instance?.RemoveTab(s_tabName);
            GameplaySetup.instance?.AddTab(s_tabName, this.ResourceName, this);
        }
        #endregion
        //ﾟ+｡*ﾟ+｡｡+ﾟ*｡+ﾟ ﾟ+｡*ﾟ+｡｡+ﾟ*｡+ﾟ ﾟ+｡*ﾟ+｡*ﾟ+｡｡+ﾟ*｡+ﾟ ﾟ+｡*ﾟ+｡｡+ﾟ*｡+ﾟ ﾟ+｡*ﾟ+｡*ﾟ+｡｡+ﾟ*｡+ﾟ ﾟ+｡*ﾟ+｡｡+ﾟ*｡+ﾟ ﾟ+｡*
        #region // プライベートメソッド
        [UIAction("#post-parse")]
        internal void PostParse()
        {
            // Code to run after BSML finishes
            this.Enable = PluginConfig.Instance.Enable;
            this.Offset = PluginConfig.Instance.OffsetValue;
        }

        protected bool SetProperty<T>(ref T field, T val, [CallerMemberName] string memberName = null)
        {
            if (EqualityComparer<T>.Default.Equals(field, val))
            {
                return false;
            }
            field = val;
            this.NotifyPropertyChanged(memberName);
            return true;
        }

        private void SettngTabView_PropertyChanged(object sender, PropertyChangedEventArgs e)
        {
            if (e.PropertyName == nameof(this.Enable))
            {
                PluginConfig.Instance.Enable = this.Enable;
            }
            else if (e.PropertyName == nameof(this.Offset))
            {
                PluginConfig.Instance.OffsetValue = this.Offset;
            }
        }
        #endregion
        //ﾟ+｡*ﾟ+｡｡+ﾟ*｡+ﾟ ﾟ+｡*ﾟ+｡｡+ﾟ*｡+ﾟ ﾟ+｡*ﾟ+｡*ﾟ+｡｡+ﾟ*｡+ﾟ ﾟ+｡*ﾟ+｡｡+ﾟ*｡+ﾟ ﾟ+｡*ﾟ+｡*ﾟ+｡｡+ﾟ*｡+ﾟ ﾟ+｡*ﾟ+｡｡+ﾟ*｡+ﾟ ﾟ+｡*
